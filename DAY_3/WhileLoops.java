package DAY_3;

public class WhileLoops {
    public static void main(String[] args) {
        int n=10;
        //while
        int i=1;
        while(i<=n){
            System.out.print(i+" ");
            i++;
        }
        System.out.println();
        //do while
        int j=10;
        do{
            System.out.println(1);
            i++;
        }while(j<=5);
    }
}
